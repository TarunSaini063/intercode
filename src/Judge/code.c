#include<stdio.h>
int main(void)
{	
	int x,y;
	scanf("%d",&x);	
	scanf("%d",&y);
	for(int i=0;i<x;i++)
		printf("%d ",i+1);
	for(int i=0;i<y;i++)
	{
		printf("%d ",i+1);
	}
	return 0;
}
